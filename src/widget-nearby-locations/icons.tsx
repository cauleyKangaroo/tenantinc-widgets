/** Gold rating stars; supports halves (e.g. 4.5 → 4 full + 1 half). */
export function StarRating({ rating, size = 16 }: { rating: number; size?: number }) {
  const uid = React.useId();
  return (
    <span className="nl-stars" style={{ display: 'inline-flex', alignItems: 'center', gap: '4px' }}>
      {[0, 1, 2, 3, 4].map((i) => {
        const v = rating - i;
        const frac = v >= 1 ? 1 : v >= 0.5 ? 0.5 : 0;
        const clipId = `${uid}-s${i}`;
        return (
          <svg key={i} width={size} height={size} viewBox="0 0 17.15 17" fill="none" aria-hidden="true">
            <path d={ROUND_STAR} fill={STAR_EMPTY} />
            {frac > 0 && (
              <>
                <defs>
                  <clipPath id={clipId}>
                    <rect x="0" y="0" width={17.15 * frac} height="17" />
                  </clipPath>
                </defs>
                <path d={ROUND_STAR} fill={STAR_GOLD} clipPath={`url(#${clipId})`} />
              </>
            )}
          </svg>
        );
      })}
    </span>
  );
}

